#include <unistd.h>
#include "malloc.h"

int main()
{
	char *s;
	int	i = 0;

	s = malloc(sizeof(char) * 15000);
	while (i < 15000)
	{
		s[i] = '\0';
		i++;
	}
	i = 0;
	while (i < 14999)
	{
		s[i] = 'a';
		i++;
	}
	s[i] = '\0';
	i = 0;
	while (s[i])
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, "\n", 1);
	return (0);
}
